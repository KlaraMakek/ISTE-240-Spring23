            <h3>Popular activities</h3>
            <p>One of the most popular activities for tourists in Hvar is hiking, as the island is home to several
                hiking trails that lead to some of its most stunning natural attractions. Visitors can hike to the
                summit of Sveti Nikola, the highest point on the island, for panoramic views of the surrounding
                coastline, or explore the lush green forests of the Pakleni Islands. <i>Top 5 tourists attractions!</i>
            </p><br>
            <ul class="liVis">
                <li><b>Hvar Fortress:</b> This historic fortress is located on a hill above Hvar town and offers
                    stunning views of the town and the surrounding islands. It was built in the 16th century to protect
                    the town from invaders.</li>
                <li><b>Stari Grad Plain:</b> This UNESCO World Heritage site is located in the town of Stari Grad and is
                    the oldest continuously cultivated plain in the world. It dates back to the 4th century BC and is
                    still used for agriculture today.</li>
                <li><b>Pakleni Islands:</b> These beautiful islands are located just off the coast of Hvar and are a
                    popular destination for boat tours and water sports. They offer stunning views of the Adriatic Sea
                    and secluded beaches.</li>
                <li><b>Franciscan Monastery and Museum:</b> This historic monastery is located in the town of Hvar and
                    houses a museum with exhibits on the island's history, culture, and art. The monastery itself dates
                    back to the 15th century and has a beautiful garden and cloister.</li>
                <li><b>Hvar Cathedral:</b> This historic cathedral is located in the heart of Hvar town and dates back
                    to the 16th century. It features a beautiful bell tower and a collection of religious art and
                    artifacts.</li>
            </ul>
