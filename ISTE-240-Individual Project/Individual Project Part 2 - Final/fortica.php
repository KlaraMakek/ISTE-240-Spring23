      <h2>About Fortica</h2>
      <p>The fortress, also known as Fortica or Španjola, is a symbol of Hvar, located on a hill above Novigrad. The
        same location previously held an Illyrian fortress built between 2000 and 1000 BC, followed by a Roman tower
        that was rebuilt in the early 13th century and gave Novigrad its first name, Castrum Novum.</p><br>
        <img src="assets/img/fol1.jpg" alt="The fortress Fortica from above." width="500" class="img">
      <p>The Gusii-Kurjakovii nobles of Lika-Krbav reconstructed the fortress at the end of the 13th century (1282) to
        protect their property near Novigrad. In 1386, the Bosnian Ban Stjepan II and his daughter Elizabeth, the widow
        of King Ludovic I of Anjou and mother of the Hungarian-Croatian Queen Marija, were imprisoned here by the
        Kotromanić.</p><br>
        <img src="assets/img/fol2.jpg" alt="The fortress Fortica." width="500" class="img">
      <p>The Republic of Venice seized Novigrad in 1409, and the fortress was significantly transformed into a castle.
        The fortress was nearly impenetrable from the early 16th century to the mid-17th century. However, in 1646, the
        Turks took control of it for nine months before it was recaptured and placed under Venetian rule. The fortress
        was last reconstructed in 1708 but was abandoned entirely in 1797, following the fall of the Venetian Republic.
        One day walking to the fortress sounds wonderful, the amazing site on the top, fresh air. You should definitely
        visit!</p><br>
