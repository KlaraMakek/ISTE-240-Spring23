      <h2>About Humac and Grapceva cave</h2>
      <p>Humac, a deserted pastoral village located 7 kilometers east of Jelsa in Hvar, is a popular tourist destination
        due to its magical atmosphere, picturesque ruins, and stunning views of the sea.</p><br>
      <p>Despite the lack of basic amenities such as electricity and running water, the village attracts a significant
        number of tourists in the summer and locals from nearby Vrisnik. The village is at its liveliest on June 26th,
        the day of Saints John and Paul, when islanders come together for a celebration.</p><br>
        <img src="assets/img/hum1.jpg" alt="Hole of the cave." width="500" class="img">
      <p>Humac is also home to a top-notch restaurant, which serves as the launchpad for excursions to Grapeva cave, the
        oldest site in the area dating back to the Neolithic era. The cave, which is surrounded by chambers and contains
        dominant stalactites and stalagmites, is an amazing sight when lit by candles.</p><br>
        <img src="assets/img/hum2.jpg" alt="Inside of the cave." width="500" class="img">
      <p>The Archaeological Museum in Hvar displays various artifacts from that era, including flint knives, hammers,
        arrows, bones of both human and animal origin, and pottery. The cave's seclusion and tranquility make it a
        perfect setting for introspection.</p><br>
        <br><h3>Three things to do in Humac:</h3>
          <ul class="liVis">
            <li><b>Visit the Ethno-Eco Village:</b> Humac is home to a unique Ethno-Eco Village, where you can learn about the traditional way of life in Dalmatia. The village features restored stone houses and a museum with exhibits on traditional crafts, such as weaving, pottery, and olive oil production.</li>
            <li><b>Hiking and Walking:</b> Humac is surrounded by beautiful nature, and there are many hiking and walking trails that you can explore. You can take a leisurely walk through the vineyards and olive groves, or take a more challenging hike to the nearby peaks for stunning views of the island.</li>
            <li><b>Taste Local Wines:</b> Humac is located in the heart of Hvar's wine country, and there are many small wineries in the area where you can taste the local wines. The wines are made from local grape varieties such as Plavac Mali, Bogdanuša, and Pošip, and many wineries offer guided tours and tastings.</li>
          </ul><br>
