      <h2>About Vrbovška</h2>
      <p>Vrboška, the smallest town on the island of Hvar, is renowned for its beautiful location in a bay surrounded by
        dense pine forests, cultural landmarks displaying Gothic, Renaissance, and Baroque styles, and architecture
        scattered throughout the town's winding streets. It is also known as "Little Venice" due to the lovely islet in
        the middle of the bay connected by bridges.</p><br>
      <img src="assets/img/vrb1.jpg" alt="Vrboska city." width="500" class="img">
      <p>The town has a rich history as a fishing community that has grown into a charming tourist destination. The
        area's unique charm is contributed by the typical Mediterranean folk architecture, distinguished old town houses
        with Gothic, Renaissance, and Baroque features, the sacred spirit of old church buildings, and opulent modern
        villas.</p><br>
      <p>Vrboška offers beautiful pebble and sand beaches, clear blue sea, clean air, and unspoiled nature, including a
        nudist island called Zeevo. Accommodation options such as apartments, family pensions, restaurants, and
        campgrounds with shops and buffets are located near the beaches.</p><br>
      <img src="assets/img/vrb2.jpg" alt="Beach near Vrboska." width="500" class="img">
      <p>Vrboška's extensive fishing heritage is reflected in the local cuisine, with healthy food from nearby fields
        and
        fresh-caught fish, homemade olive oil, and aromatic spices. It pairs well with Hvar's renowned wines. In terms
        of lodging options, visitors can choose from private boarding houses, the Nudist Camp with a nudist beach and
        other amenities, or the ACI Marina Vrboška, which is open to boaters year-round.</p><br>
      <br>
      <h3>Three things to do in Vrbovška:</h3>
      <ul class="liVis">
        <li><b>Visit the Vrboska Fisherman's Museum:</b> Vrboska has a long history of fishing, and the Vrboska
          Fisherman's Museum is a great place to learn about the local fishing traditions and see the tools and
          equipment used by fishermen in the past. The museum is located in a historic building in the center of town
          and features exhibits on the history of the town and the fishing industry.</li>
        <li><b>Rent a Boat and Explore the Harbor:</b> Vrboska is a picturesque fishing village with a beautiful harbor
          that is perfect for boating. You can rent a boat from one of the local rental shops and explore the harbor,
          visit nearby coves and beaches, and enjoy the stunning views of the surrounding landscape.</li>
        <li><b>Walk the Vrboska Promenade:</b> Vrboska has a lovely promenade that runs along the harbor and is lined
          with palm trees and colorful fishing boats. You can take a leisurely walk along the promenade, enjoy the sea
          breeze, and admire the local architecture, including the 16th-century Church of St. Lawrence and the
          Venetian-style houses.
        </li>
      </ul>
