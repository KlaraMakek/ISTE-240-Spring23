      <h2>About Pokonji dol</h2>
      <p>Pokonji Dol, a small islet with a lighthouse from 1872, is located just two kilometers from the town of Hvar
        and has become a popular subject for artists due to its unique appearance. The islet is only 150 meters wide but
        has several hundred meters of coastline, making it an interesting destination for both domestic and
        international visitors. Pokonji Dol serves as a starting point for sailors traveling to Korula, Lastovo, and
        Mljet, and a beacon of safe leeward on their return voyages.</p><br>
      <img src="assets/img/pok1.jpg" alt="Pokonja beach." width="500" class="img">
      <p>Originally, the lighthouse was just a harbor light but became automated over time. Pokonji Dol is part of the
        Pakleni or Hellish islands, which are named after pine resin used to protect wooden boats and ships. It is the
        easternmost point of the islands, with Veli Vodnjak to the west, Cape Moƒçiguzica to the north on Sveti Klement,
        and the Islet of Stambedar to the south. Visitors can easily spot the islet while walking along the seafront
        promenade in Hvar and can even reach for it as it appears to be very close.</p><br>
