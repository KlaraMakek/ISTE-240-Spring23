      <h3>Three things to do in Pokonji dol:</h3>
      <ul class="liVis">
        <li><b>Swimming and Sunbathing:</b> Pokonji Dol is a beautiful pebble beach with crystal-clear waters that are perfect for swimming and sunbathing. You can relax on the beach, rent sun loungers and umbrellas, or enjoy water sports such as kayaking, paddleboarding, and snorkeling.</li>
        <li><b>Take a Boat Tour:</b> There are many boat tours available from Pokonji Dol that take you to nearby islands and hidden coves. You can take a half-day or full-day tour and explore the beautiful Adriatic Sea, swim in secluded bays, and see some of the stunning coastal landscapes.</li>
        <li><b>Visit the Franciscan Monastery:</b> The Franciscan Monastery is located just a short distance from Pokonji Dol and is worth a visit. The monastery features a beautiful Baroque church, a museum with exhibits on local history and culture, and a picturesque garden with views of the sea.</li>
